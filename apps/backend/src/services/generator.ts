import type { ClaimReport, DemoPlan, DemoRequest, GeneratedFile } from "../types.js";

export async function generateDemoFiles(
  input: DemoRequest,
  plan: DemoPlan,
  claimReport: ClaimReport
): Promise<GeneratedFile[]> {
  const safeClaims = claimReport.claims.map((claim: any) => claim.rewrite ?? claim.text);

  const appTsx = `
import { useState } from "react";
import "./style.css";

const mockFields = [
  { name: "claim_number", value: "CLM-2026-001", confidence: 0.98 },
  { name: "policyholder_name", value: "Jane Rivera", confidence: 0.94 },
  { name: "service_date", value: "2026-05-19", confidence: 0.91 },
  { name: "amount", value: "1420.55", confidence: 0.88 }
];

export default function App() {
  const [step, setStep] = useState(0);
  const screens = ${JSON.stringify(plan.screens, null, 2)};

  return (
    <main className="shell">
      <section className="hero">
        <p className="eyebrow">Generated by ProofPilot</p>
        <h1>${escapeForTemplate(plan.title)}</h1>
        <p>${escapeForTemplate(plan.story)}</p>
      </section>

      <section className="grid">
        <aside className="card">
          <h2>Demo flow</h2>
          {screens.map((screen, index) => (
            <button key={screen} className={index === step ? "active step" : "step"} onClick={() => setStep(index)}>
              {index + 1}. {screen}
            </button>
          ))}
        </aside>

        <section className="card wide">
          <h2>{screens[step]}</h2>
          {step === 0 && <p>This walkthrough shows how ${escapeForTemplate(input.industry)} leaders could evaluate ${escapeForTemplate(input.apiName)} in a realistic workflow.</p>}
          {step === 1 && <div className="upload">Drop sample claim PDF here<br/><span>Mock mode enabled</span></div>}
          {step === 2 && <FieldTable />}
          {step === 3 && <p>Reviewer confirms low-confidence values, edits fields if needed, and approves the structured output.</p>}
          {step === 4 && <pre>{JSON.stringify({ exported: true, fields: mockFields }, null, 2)}</pre>}
        </section>
      </section>

      <section className="card">
        <h2>Source-grounded claims</h2>
        <ul>{${JSON.stringify(safeClaims)}.map((claim) => <li key={claim}>{claim}</li>)}</ul>
      </section>
    </main>
  );
}

function FieldTable() {
  return <table><thead><tr><th>Field</th><th>Value</th><th>Confidence</th></tr></thead><tbody>{mockFields.map(f => <tr key={f.name}><td>{f.name}</td><td>{f.value}</td><td>{Math.round(f.confidence * 100)}%</td></tr>)}</tbody></table>;
}
`;

  return [
    { path: "README.md", content: readme(input, plan) },
    { path: "demo-script.md", content: demoScript(plan) },
    { path: "claim-check-report.md", content: claimReportMarkdown(claimReport) },
    { path: "frontend/package.json", content: JSON.stringify({ scripts: { dev: "vite", build: "vite build", preview: "vite preview" }, dependencies: { "@vitejs/plugin-react": "latest", vite: "latest", typescript: "latest", react: "latest", "react-dom": "latest" }, devDependencies: {} }, null, 2) },
    { path: "frontend/index.html", content: `<div id="root"></div><script type="module" src="/src/main.tsx"></script>` },
    { path: "frontend/src/main.tsx", content: `import React from "react";\nimport { createRoot } from "react-dom/client";\nimport App from "./App";\ncreateRoot(document.getElementById("root")!).render(<React.StrictMode><App /></React.StrictMode>);\n` },
    { path: "frontend/src/App.tsx", content: appTsx },
    { path: "frontend/src/style.css", content: css() },
    { path: "backend/package.json", content: JSON.stringify({ type: "module", scripts: { dev: "node src/server.js" }, dependencies: { express: "latest", cors: "latest" } }, null, 2) },
    { path: "backend/src/server.js", content: backendServer() }
  ];
}

function readme(input: DemoRequest, plan: DemoPlan) {
  return `# ${plan.title}\n\nGenerated API demo for **${input.industry}** using **${input.apiName}**.\n\n## Story\n\n${plan.story}\n\n## Screens\n\n${plan.screens.map((s) => `- ${s}`).join("\n")}\n\n## Endpoints Used\n\n${plan.endpointsUsed.map((e) => `- \`${e}\``).join("\n")}\n\n## Run\n\n\`\`\`bash\ncd frontend\nnpm install\nnpm run dev\n\`\`\`\n`;
}

function demoScript(plan: DemoPlan) {
  return `# Demo Script\n\n1. Open with the business problem.\n2. Show the generated workflow: ${plan.screens.join(" → ")}.\n3. Explain the API calls used.\n4. Show the claim-check report to prove unsupported claims were removed.\n5. Close with implementation next steps.\n`;
}

function claimReportMarkdown(report: ClaimReport) {
  return `# Claim Check Report\n\n| Claim | Status | Rewrite |\n|---|---|---|\n${report.claims.map((c: any) => `| ${c.text} | ${c.status} | ${c.rewrite ?? ""} |`).join("\n")}\n`;
}

function backendServer() {
  return `import express from "express";\nimport cors from "cors";\nconst app = express();\napp.use(cors());\napp.get('/', (_req, res) => res.json({ status: "ok", message: "ProofPilot Generated Demo Backend API is running." }));\napp.get('/api/mock/extraction', (_req,res)=>res.json({status:'completed', fields:[{name:'claim_number', value:'CLM-2026-001', confidence:0.98}]}));\napp.listen(3001,()=>console.log('Generated demo backend on http://localhost:3001'));\n`;
}

function css() {
  return `body{margin:0;font-family:Inter,system-ui,sans-serif;background:#f7f7fb;color:#18181b}.shell{max-width:1100px;margin:auto;padding:32px}.hero,.card{background:white;border-radius:24px;padding:24px;box-shadow:0 12px 35px rgba(0,0,0,.08);margin-bottom:20px}.eyebrow{text-transform:uppercase;letter-spacing:.14em;font-size:12px;color:#6366f1}.grid{display:grid;grid-template-columns:320px 1fr;gap:20px}.wide{min-height:360px}.step{display:block;width:100%;text-align:left;margin:8px 0;padding:12px;border:1px solid #ddd;border-radius:14px;background:#fff;cursor:pointer}.active{border-color:#18181b;background:#f1f5f9}.upload{border:2px dashed #bbb;border-radius:18px;padding:48px;text-align:center;color:#555}table{width:100%;border-collapse:collapse}td,th{border-bottom:1px solid #e5e7eb;padding:10px;text-align:left}pre{background:#0f172a;color:#e2e8f0;padding:16px;border-radius:14px;overflow:auto}`;
}

function escapeForTemplate(value: string) {
  return value.replace(/`/g, "'").replace(/\$/g, "");
}
